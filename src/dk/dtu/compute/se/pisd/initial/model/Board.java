package dk.dtu.compute.se.pisd.initial.model;

import com.sun.istack.internal.NotNull;
import dk.dtu.compute.se.pisd.designpatterns.observer.Subject;

import java.util.ArrayList;
import java.util.List;

import static dk.dtu.compute.se.pisd.initial.model.Phase.INITIALISATION;

/**
 * ...
 *
 * @author Ekkart Kindler, ekki@dtu.dk
 *
 */
public class Board extends Subject {

    public final int width;
    public final int height;

    private Space[][] spaces;

    private List<Player> players = new ArrayList<Player>(); // vi ved ikke på forhånd hvor mange spiller der skal spille, hvorfor er det
    // oplagt er at bruge list. Play er en type af klassen Play.
    private Player current;

    private Phase phase = INITIALISATION;
    private int step = 0;

    private boolean stepMode;

    public Board(int width, int height) {
        this.width = width;
        this.height = height;
        spaces = new Space[width][height];
        for (int x = 0; x < width; x++) {
            for(int y = 0; y < height; y++) {
                Space space = new Space(this, x, y);
                spaces[x][y] = space;
            }
        }
        this.stepMode = false;
    }

    public Space getSpace(int x, int y) {
        if (x >= 0 && x < width &&
                y >= 0 && y < height) {
            return spaces[x][y];
        } else {
            return null;
        }
    }

    public int getPlayersNumber() {
        return players.size();
    }

    public void addPlayer(@NotNull Player player) { // her tilføjes der spiller. Der må ikke kaldes på spilleren med parameteren 0.
        if (player.board == this && !players.contains(player)) { // contains(en kommando) er en del af arraylist. Samme spiller må ikke være med 2 gange.
            // (player) referer til parameteren player.
            players.add(player);
            notifyChange(); // så snart der kommer ændringer, opdatere denne kode ændringen.
        }
    }

    public Player getPlayer(int i) {
        if (i >= 0 && i < players.size()) {
            return players.get(i);
        } else {
            return null;
        }
    }

    public Player getCurrentPlayer() {
        return current;
    }

    public void setCurrentPlayer(Player player) {
        if (player != this.current && players.contains(player)) {
            this.current = player;
            notifyChange();
        }
    }

    public Phase getPhase() {
        return phase;
    }

    public void setPhase(Phase phase) {
        if (phase != this.phase) {
            this.phase = phase;
            notifyChange();
        }
    }

    public int getStep() {
        return step;
    }

    public void setStep(int step) {
        if (step != this.step) {
            this.step = step;
            notifyChange();
        }
    }

    public boolean isStepMode() {
        return stepMode;
    }

    public void setStepMode(boolean stepMode) {
        if (stepMode != this.stepMode) {
            this.stepMode = stepMode;
            notifyChange();
        }
    }

}
